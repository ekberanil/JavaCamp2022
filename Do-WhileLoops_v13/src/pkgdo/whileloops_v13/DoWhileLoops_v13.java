
package pkgdo.whileloops_v13;


public class DoWhileLoops_v13 {


    public static void main(String[] args) {
        //Do-While Loops
        int i=12;
        do{
            System.out.println(i);
            i+=2;
        }while(i<10);
        
        System.out.println("Do-While dongusu calisti");
    }
    
}
