
package whileloops_v12;


public class WhileLoops_v12 {

  
    public static void main(String[] args) {
        // While Loops
        
        int i=1;
        while(i<10){
            System.out.println(i);
            i++;
            
        }
        System.out.println("While Dongusu Bitti");
        int a=1;
        while(a<10){
            System.out.println(a);
            a+=2;
            
        }
        System.out.println("Tek Sayilarin While Dongusu Bitti");
        int b=0;
        while(b<10){
            System.out.println(b);
            b+=2;
            
        }
        System.out.println("Cift Sayilarin While Dongusu Bitti");
    }
    
}
