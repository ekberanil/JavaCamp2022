
package forloops_v11;

public class ForLoops_v11 {

   
    public static void main(String[] args) {
        // For Loops
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println("Dongu Bitti");
        
        for(int i=0;i<10;i+=2){
            System.out.println(i);
        }
        System.out.println("Cift Sayilar Dongusu Bitti");
        
         for(int i=1;i<10;i+=2){
            System.out.println(i);
        }
        System.out.println("Tek Sayilar Dongusu Bitti");
    }
    
}
